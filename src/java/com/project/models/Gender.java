package com.project.models;

/**
 *
 * @author Ahmada Yusril
 */
public class Gender {
    public Long value;
    public String label;

    public Gender(Long value, String label) {
        this.value = value;
        this.label = label;
    }

    public Long getValue() {
        return value;
    }

    public void setValue(Long value) {
        this.value = value;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
